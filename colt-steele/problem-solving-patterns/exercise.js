// Same
<p>
  Write a function called same, which accepts two arrays. The function should
  return true if every value in the array has it's corresponding value squared
  in the second array. The frequency of values must be the same.
</p>;

// Anagrams
<p>
  Given two strings, write a function to determine if the second string is an
  anagram of the first. An anagram is a word, phrase, or name formed by
  rearranging the letters of another, such as cinema, formed from iceman.
</p>;

// sameFrequency
<p>
  Write a function called sameFrequency. Given two positive integers, find out
  if the two numbers have the same frequency of digits.
</p>;

// sumZero
<p>
  Write a function called sumZero which accepts a sorted array of integers. The
  function should find the first pair where the sum is 0. Return an array that
  includes both values that sum to zero or undefined if a pair does not exist.
</p>;

// countUniqueValues
<p>
  Implement a function called countUniqueValues, which accepts a sorted array,
  and counts the unique values in the array. There can be negative numbers in
  the array, but it will always be sorted.
</p>;

// areThereDuplicates
<p>
  Implement a function called, areThereDuplicates which accepts a variable
  number of arguments, and checks whether there are any duplicates among the
  arguments passed in. You can solve this using the frequency counter pattern OR
  the multiple pointers pattern.
</p>;

// averagePair
<p>
  Write a function called averagePair. Given a sorted array of integers and a
  target average, determine if there is a pair of values in the array where the
  average of the pair equals the target average. There may be more than one pair
  that matches the average target.
</p>;

// isSubsequence
<p>
  Write a function called isSubsequence which takes in two strings and checks
  whether the characters in the first string form a subsequence of the
  characters in the second string. In other words, the function should check
  whether the characters in the first string appear somewhere in the second
  string, without their order changing.
</p>;

// maxSubarraySum
<p>
  Given an array of integers and a number, write a function called
  maxSubarraySum, which finds the maximum sum of a subarray with the length of
  the number passed to the function. Note that a subarray must consist of
  consecutive elements from the original array.
</p>;

// minSubarrayLen
<p>
  Write a function called minSubarrayLen which accepts two parameters - an array
  of positive integers and a positive integer. This function should return the
  minimal length of a contiguous subarray of which the sum is greater than or
  equal to the integer passed to the function. If there isn't one, return 0
  instead.
</p>;

// findLongestSubstring
<p>
  Write a function called findLongestSubstring, which accepts a string and
  returns the length of the longest substring with all distinct characters.
</p>;

// search
<p>
  Given a sorted array of integers, write a function called search, that accepts
  a value and returns the index where the value passed to the function is
  located. If the value is not found, return -1
</p>;

// power
<p>
  Write a function called power which accepts a base and an exponent. The
  function should return the power of the base to the exponent. This function
  should mimic the functionality of Math.pow() - do not worry about negative
  bases and exponents.
</p>;

// factorial
<p>
  Write a function factorial which accepts a number and returns the factorial of
  that number. A factorial is the product of an integer and all the integers
  below it; e.g., factorial four 4! is equal to 24, because 4 * 3 * 2 * 1 equals
  24. factorial zero (0!) is always 1.
</p>;

// productOfArray
<p>
  Write a function called productOfArray which takes in an array of numbers and
  returns the product of them all.
</p>;

// recursiveRange
<p>
  Write a function called recursiveRange which accepts a number and adds up all
  the numbers from 0 to the number passed to the function
</p>;

// fib
<p>
  Write a recursive function called fib which accepts a number and returns the n
  th number in the Fibonacci sequence. Recall that the Fibonacci sequence is the
  sequence of whole numbers 1, 1, 2, 3, 5, 8, ... which starts with 1 and 1, and
  where every number thereafter is equal to the sum of the previous two numbers.
</p>;

// isPalindrome
<p>
  Write a recursive function called isPalindrome which returns true if the
  string passed to it is a palindrome (reads the same forward and backward).
  Otherwise it returns false.
</p>;

// someRecursive
<p>
  Write a recursive function called someRecursive which accepts an array and a
  callback. The function returns true if a single value in the array returns
  true when passed to the callback. Otherwise it returns false.
</p>;

// flatten
<p>
  Write a recursive function called flatten which accepts an array of arrays and
  returns a new array with all values flattened.
</p>;

// capitalizeFirst
<p>
  Write a recursive function called capitalizeFirst. Given an array of strings,
  capitalize the first letter of each string in the array.
</p>;

// nestedEvenSum
<p>
  Write a recursive function called nestedEvenSum. Return the sum of all even
  numbers in an object which may contain nested objects.
</p>;

// capitalizeWords
<p>
  Write a recursive function called capitalizeWords. Given an array of words,
  return a new array containing each word capitalized.
</p>;

// stringifyNumbers
<p>
  Write a function called stringifyNumbers which takes in an object and finds
  all of the values which are numbers and converts them to strings. Recursion
  would be a great way to solve this!
</p>;

// collectStrings
<p>
  Write a function called collectStrings which accepts an object and returns an
  array of all the values in the object that have a typeof string
</p>;

// binarySearch
<p>
  Write a function called binarySearch which accepts a sorted array and a value
  and returns the index at which the value exists. Otherwise, return -1.
</p>;
